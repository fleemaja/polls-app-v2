// vote
export function vote(index, optionIndex) {
  return {
    type: 'INCREMENT_VOTES',
    index,
    optionIndex
  }
}

// add comment
export function addComment(postId, author, comment) {
  return {
    type: 'ADD_COMMENT',
    postId,
    author,
    comment
  }
}

// remove comment

export function removeComment(postId, i) {
  return {
    type: 'REMOVE_COMMENT',
    i,
    postId
  }
}
